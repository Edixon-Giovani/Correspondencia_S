    private void jTextDucKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextDucKeyTyped
        // TODO add your handling code here:
        char caracter = evt.getKeyChar();
        if(caracter < '0' || caracter > '9'){
            evt.consume();
        }
        String Cadena = jTextDuc.getText();
        if (Cadena.length() >= 11) {
            evt.consume();
            int Long = this.jTextDuc.getText().length();
            if (Long <= 11){
                JOptionPane.showMessageDialog(null, "Erro solo puede ingresar Once digitos","Error",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jTextDucKeyTyped

