         private void AsignarFecha(){
            Calendar fecha = new GregorianCalendar();
            String hoy = "";
            int año = fecha.get(Calendar.YEAR);
            int mes = fecha.get(Calendar.MONTH)+1;
            int dia = fecha.get(Calendar.DAY_OF_MONTH);
            int hora = fecha.get(Calendar.HOUR_OF_DAY);
            int minuto = fecha.get(Calendar.MINUTE);
            int segundo = fecha.get(Calendar.SECOND);
            hoy = dia+"/"+mes+"/"+año+" "+hora+":"+minuto+":"+segundo; 
             
             try{   
                    SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
                    Date fechaDate = new Date();
                    fechaDate = formato.parse(hoy);              
                                                          
                    this.jDateFchaE.setDate(fechaDate);
                    jDateFchaE.getDateEditor().setEnabled(false);
                    this.jDateFchaElab.setDate(fechaDate);
                    jDateFchaElab.getDateEditor().setEnabled(false);
                   
                    
                 
             }catch (ParseException ex){
             Logger.getLogger(FrmCorresp_Salida.class.getName()).log(Level.SEVERE,null, ex);
         }
             
         }
